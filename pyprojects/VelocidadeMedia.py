###Velocidade Media###
###Calcular a Velocidade Media (Vm) de um automóvel que percorreu uma distância (D) em um determinado tempo (T)###

print ("P2 INFO - PHYTON")
print ("Cálculo de Velocidade Média")
D = int(input("Informe a Distância (D) em Km: "))
T = int(input("Informe o tempo (T) em horas: "))
VelocidadeMedia =  D / T
print (f"Velocidade Média: {VelocidadeMedia}")


